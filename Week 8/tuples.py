my_tuple=(1, 2, 3, 4, 5)

print(my_tuple[0]) # Output: 1
print(my_tuple[1]) # Output: 2
print(my_tuple[2]) # Output: 3  
print(my_tuple[3]) # Output: 4
print(my_tuple[4]) # Output: 5
print(my_tuple[-1]) # Output: 5 (last element)

print(my_tuple[1:3]) # Output: (2, 3) (slice from index 1 to 2)
print(my_tuple[:3]) # Output: (1, 2, 3) (slice from start to index 2)

